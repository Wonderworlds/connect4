#include "libft.h"

char	**ft_free_tab(char **spt)
{
	int		i;

	i = 0;
	if (spt)
	{
		while (spt[i])
		{
			free(spt[i]);
			i++;
		}
		free(spt);
	}
	return (NULL);
}
